public class Cor {
    private int r, g, b;
    
    public Cor(int red, int green, int blue) {
        r = red;
        g = green;
        b = blue;
    }
    
    public int getR() {
        return r;
    }
    
    public int getG() {
        return g;
    }
    
    public int getB() {
        return b;
    }
    
    public void setR(int novoR) {
        r = novoR;
    }
    
    public void setG(int novoG) {
        g = novoG;
    }
    
    public void setB(int novoB) {
        b = novoB;
    }
}