public class Cor {
    private int red, green, blue;
    
    public Cor(int red, int green, int blue) {
        this.red = red;
        this.green = green;
        this.blue = blue;
    }
    
    public int getRed() {
        return red;
    }
    
    public int getGreen() {
        return green;
    }
    
    public int getBlue() {
        return blue;
    }
    
    public void setRed(int novoR) {
        red = novoR;
    }
    
    public void setGreen(int novoG) {
        green = novoG;
    }
    
    public void setBlue(int novoB) {
        blue = novoB;
    }
}